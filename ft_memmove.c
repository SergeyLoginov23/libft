#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char	*ptr;
	unsigned char	*ptr2;
	unsigned int	i;

	i = 0;
	if ((dest == 0 & src == 0))
		return (0);
	ptr = (unsigned char*)dest;
	ptr2 = (unsigned char*)src;
	if (ptr2 < ptr)
		while (++i <= n)
			ptr[n - i] = ptr2[n - i];
	else
		while (n-- > 0)
			*(ptr++) = *(ptr2++);
	return (dest);
}
